<p>Domy.pl to najnowszy portal Grupa Morizon Sp. z o.o., zawierający ponad dwa miliony ofert sprzedaży oraz wynajmu nieruchomości z rynku pierwotnego i wtórnego, a także rozbudowany katalog firm deweloperskich i agencji nieruchomości.</p>
<p>Portal stworzony został na bazie wieloletnich doświadczeń oraz badań użyteczności i funkcjonalności. Posiada nowoczesną, rozbudowaną wyszukiwarkę, ułatwiającą użytkownikom poruszanie się po rynku nieruchomości. Każda oferta zawiera szczegółowy opis budynku bądź lokalu, jego zdjęcia, oznaczenie lokalizacji na mapie oraz formularz, pozwalający na szybki kontakt z oferentem, a także jego dane teleadresowe.</p>
<p style="margin-bottom: 0cm;">Dla wygody użytkowników portal domy.pl oferuje usługę szybkiego powiadamiania, dzięki której każdego dnia aktualne oferty, wybrane na podstawie uzupełnionych przez nich kryteriów, trafią bezpośrednio do skrzynki e-mailowej.</p>
<p style="margin-bottom: 0cm;">Plany rozwoju portalu przewidują jego stałą rozbudowę w celu stworzenia kompleksowej bazy wiedzy, zawierającej wszystkie niezbędne informacje dla osób, będących w trakcie procesu kupowania, sprzedawania, bądź wynajmowania nieruchomości</p> </section>
