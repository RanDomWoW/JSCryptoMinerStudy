                        <p>Oto zestawienie najczęściej popełnianych błędów na egzaminie.  The most common mistakes: WRITING Writing part has not been carefully planned by the student. I to niestety widać. Musisz sobie uświadomić, że forma pisemna na maturze to nie list do ukochanego/ukochanej ani e-mail w sprawie obiadów w szkolnej stołówce. Tu musisz dokładnie rozplanować co chcesz zawrzeć w swojej wypowiedzi, w jakiej kolejności, za pomocą jakich czasów i słów. Nie trzeba chyba wspominać o absolutnej konieczności zastosowania się do punktów wymienionych w poleceniu!</p>
  
