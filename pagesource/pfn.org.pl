                                <h2>
                                    <b>
                                         O Fundacji                                    </b>
                                </h2>
                                <p>Dla nas Polak&oacute;w nie ma rzeczy niemożliwych &ndash; tak jest od zawsze. Chcemy pokazać naszą solidarność, wrażliwość, gościnność, przedsiębiorczość, kreatywność, pracowitość i determinację.</p>                                <p>Pragniemy promować nasze sukcesy w nauce, bogatą kulturę, wspaniałą historię i niepowtarzalną przyrodę. Taką misję ma Polska Fundacja Narodowa.<br /><br />Polska to kraj ludzi o wielkiej wiedzy i umiejętnościach &ndash; słynnego astronoma Mikołaja Kopernika, inżyniera Ernesta Malinowskiego budującego koleje w Andach czy odkrywczyni nowych pierwiastk&oacute;w Marii Skłodowskiej-Curie. Ojczyzna wynalazc&oacute;w radiotelegrafu, kamer czy nawet wycieraczek do samochodu.<br /><br />Polska to kraj dzielnych ludzi, kt&oacute;rzy jak kr&oacute;l Jan III Sobieski, Tadeusz Kościuszko, Kazimierz Pułaski, J&oacute;zef Bem czy lotnicy z Dywizjonu 303 walczyli w imię wolności innych narod&oacute;w. W czasie II wojny światowej Polacy stworzyli największą podziemną armię walczącą z hitlerowskimi Niemcami. Podczas zimnej wojny odważnie rzucili wyzwanie komunizmowi, powołując niezależny związek zawodowy &bdquo;Solidarność&rdquo;.<br /><br />Polska to ojczyzna ludzi kultury &ndash; kompozytora Fryderyka Chopina, pianist&oacute;w Artura Rubinsteina czy Ignacego Paderewskiego. To także kraj ojczysty wielkiego papieża, świętego Jana Pawła II.</p>                                
