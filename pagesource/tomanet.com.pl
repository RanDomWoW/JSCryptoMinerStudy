				<div class="entry-meta">
			<span class="posted-on"><a href="http://www.tomanet.com.pl/pozyczki-warto-je-porownywac/" rel="bookmark"><time class="entry-date published" datetime="2017-01-26T17:05:33+00:00">Styczeń 26, 2017</time><time class="updated" datetime="2017-02-11T17:04:56+00:00">Luty 11, 2017</time></a></span><span class="byline"> <span class="author vcard"><a class="url fn n" href="http://www.tomanet.com.pl/author/admin/">Tomasz Net</a></span></span>		</div><!-- .entry-meta -->
