                                                        <h1 class="cp-page-title">Strona główna</h1>
                            <p>Pierwsze państwowe egzaminy certyfikatowe z języka polskiego jako obcego odbyły się w 2004 roku. To wydarzenie wielkiej wagi dla promocji języka polskiego było ukoronowaniem wieloletniej pracy zespołu specjalistów – przedstawicieli ośrodków akademickich zajmujących się kształceniem obcokrajowców w Polsce.</p>
<p>Ogólne ramy urzędowego poświadczania znajomości języka polskiego i wydawania certyfikatów określała wówczas ustawa z 11 kwietnia 2003 r. (Ustawa o zmianie ustawy o języku polskim – Dziennik Ustaw RP nr 73 z 30 kwietnia 2003 r.). Dwa szczegółowe przepisy (Rozporządzenia Ministra Edukacji Narodowej i Sportu z dnia 15 października 2003 r. w sprawie Państwowej Komisji Poświadczania Znajomości Języka Polskiego jako Obcego oraz w sprawie egzaminów języka polskiego jako obcego – Dziennik Ustaw RP nr 191 z 12 listopada 2003 r.) do tej ustawy wydane w tym samym roku określały zadania i regulamin działania Państwowej Komisji Poświadczania Znajomości Języka Polskiego jako Obcego i państwowych komisji egzaminacyjnych oraz szczegółowe warunki przeprowadzania egzaminu i wydawania poświadczeń zwanych certyfikatami znajomości języka polskiego, a także standardy wymagań dla poszczególnych poziomów zaawansowania. Wymienione akty prawne były przez 11 lat (2004-2015) podstawą przeprowadzania egzaminów dla dorosłych na trzech poziomach zaawansowania: B1, B2 i C2. W tym okresie do egzaminu z języka polskiego przystąpiło 10 029 osób.</p>
<p>W 2015 r. rozpoczęto reformę systemu. Podstawowymi aktami prawnymi, na których opiera się obecny system jest ustawa o języku polskim (zob. poniżej) oraz dwa rozporządzenia regulujące funkcjonowanie Państwowej Komisji do spraw Poświadczania Znajomości Języka Polskiego jako Obcego (zob. poniżej) oraz zasady organizacji egzaminów (zob. poniżej). Przywołane akty prawne przewidują przeprowadzanie egzaminów na 6 poziomach zaawansowania językowego: A1 – C2 w grupie dostosowanej do potrzeb dorosłych oraz A1 – B2 w grupie dostosowanej do potrzeb dzieci i młodzieży. Rozporządzenie o egzaminach określa również zasady wydawania certyfikatów bez zdawania egzaminu, o które wnioskować mogą absolwenci polskich szkół ponadgimnazjalnych, średnich oraz wyższych.</p>
