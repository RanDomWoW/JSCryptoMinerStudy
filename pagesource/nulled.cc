function EstFE_replaceText (callback) {

    if (document.selection) {
        // --- IE4+
        var s = document.selection.createRange(); 
        if (s.text) {
            eval('s.text = ' + callback + '(s.text);');
            s.select();
            return true;
        }
    } else if (typeof (obj.selectionStart) == 'number') {
        // --- Mozilla/Gecko
        var start = obj.selectionStart;
        var end = obj.selectionEnd;
        var __selection = obj.value.substr(start, end-start).toString();
        
        eval('var rs = ' + callback + '(obj.value.substr(start, end-start));');
        obj.value = obj.value.substr(0, start) + rs + obj.value.substr(end);
        
        if (rs.length > __selection.length) {
            end += rs.length - __selection.length;
        }
        
        obj.setSelectionRange(end, end);
        
        return true;
    }

    return false;
}


