  <div class="views-field views-field-body">        <div class="field-content read-more"><p>1. The Union Bank of Burma was established on 3rd April 1948 by the Act of Union Bank of Burma 1947 and took over the functions of the Yangon branches of the Reserve Bank of India. It did not have the full power of a central bank. In 1st July 1952, the Union Bank of Burma Act was enacted. In accordance with this Act Burma Currency Board was abolished and the Union Bank of Burma Act, 1952, conferred on the Union Bank of Burma all the usual functions of a central bank. The Union Bank of Burma was opened at the corner of Merchant Road and Sule Pagoda Road and had a sole right of currency issue. It was transferred from the Burma Currency Board to the newly created Currency Department of the Union Bank of Burma with effect from 1st July, 1952.</p>
<p>2. The Union Bank of Burma was established with an authorized capital of 40 million Kyats and a paid-up capital of 10 million Kyats by the Government. The People’s Bank of the Union of Burma Act was enacted in 1967 and a monolithic bank known as the People’s Bank of the Union of Burma was established in 1969 with the initial paid-up capital of 200 million Kyats that was fully contributed by the State.</p>
<p>3. After Myanmar adopted the socialist economic system in 1962, all banks were nationalized. Under the People’s Bank of the Union of Burma Act 1967, a monolithic bank was formed. After reformed the administrative system in 1972, the Union Bank of Burma Law of 1975 was promulgated and the banking system was recognized. The name of the People’s Bank of Union Bank of Burma was changed to the Union of Burma Bank. Consequently, four banks, namely the Union of Burma Bank, Myanma Economic Bank, Myanma Foreign Trade Bank and Myanma Agriculture Bank had emerged. Accordance with the 1975 Bank Law, initial paid-up capital of the Union Bank of Burma was 200 million Kyats that was fully contributed by the State.</p>
