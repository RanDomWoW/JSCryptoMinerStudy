			<h2 class="entry-title"><a href="http://lowcyburz.pl/2018/03/12/darowizny-dla-stowarzyszenia-skywarn-polska-w-2017-roku/" title="Bezpośredni odnośnik do Darowizny dla Stowarzyszenia Skywarn Polska w 2017 roku" rel="bookmark">Darowizny dla Stowarzyszenia Skywarn Polska w 2017 roku</a></h2>

			<div class="entry-meta">
				<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="http://lowcyburz.pl/2018/03/12/darowizny-dla-stowarzyszenia-skywarn-polska-w-2017-roku/" title="13:56" rel="bookmark"><span class="entry-date">12 marca 2018</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="http://lowcyburz.pl/author/artur_surowiecki/" title="Zobacz wszystkie wpisy, których autorem jest Artur Surowiecki">Artur Surowiecki</a></span>			</div><!-- .entry-meta -->

				<div class="entry-content">
				<p><a href="http://lowcyburz.pl/wp-content/uploads/2018/03/27459013_10155944477627332_2614209014744254123_n.jpg"><img class="alignright size-medium wp-image-17592" src="http://lowcyburz.pl/wp-content/uploads/2018/03/27459013_10155944477627332_2614209014744254123_n-225x300.jpg" alt="" width="225" height="300" srcset="http://lowcyburz.pl/wp-content/uploads/2018/03/27459013_10155944477627332_2614209014744254123_n-225x300.jpg 225w, http://lowcyburz.pl/wp-content/uploads/2018/03/27459013_10155944477627332_2614209014744254123_n.jpg 720w" sizes="(max-width: 225px) 100vw, 225px" /></a>Od początku ubiegłego roku kalendarzowego istnieje możliwość wpłat darowizn na cele Stowarzyszenia Skywarn Polska. Więcej szczegółów o możliwościach wspomożenia naszego Stowarzyszenia można znaleźć <a href="http://lowcyburz.pl/zostan-naszym-darczynca/">NA TEJ STRONIE</a>. Jeżeli doceniacie Państwo naszą działalność, wspierajcie ją właśnie w ten sposób!</p>
<p>W 2017 roku Stowarzyszenie Skywarn Polska zostało obdarowane darowiznami o łącznej kwocie w wysokości <strong>6722,00 zł</strong>, co stanowiło 71,3% całkowitego przychodu Stowarzyszenia za ten rok. Pozostałą część przychodu stanowiły składki członkowskie. Z darowizn zostały pokryte m.in. koszty organizacji konferencji naukowej towarzyszącej VIII Ogólnopolskiemu Zlotowi Polskich Łowców Burz. Łącznie z kwoty Państwa darowizn wydaliśmy <strong>2102,60 zł</strong>. Reszta środków pieniężnych z darowizn (tj. <strong>4619,40 zł</strong>) pozostała do dyspozycji na finansowanie niektórych celów Stowarzyszenia w bieżącym roku i w latach kolejnych. Z uwagi na plany rozwoju działalności Stowarzyszenia, środki te już wkrótce zostaną wykorzystane.</p>
