			<div class="entry-meta">
				<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="http://lowcyburz.pl/2018/01/18/prognoza-konwekcyjna-na-dzien-18-01-2018-i-na-noc-18-19-01-2018/" title="00:26" rel="bookmark"><span class="entry-date">18 stycznia 2018</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="http://lowcyburz.pl/author/artur_surowiecki/" title="Zobacz wszystkie wpisy, których autorem jest Artur Surowiecki">Artur Surowiecki</a></span>			</div><!-- .entry-meta -->

				<div class="entry-content">
				<p><strong>PROGNOZA SIŁY I AKTYWNOŚCI BURZ DLA POLSKI<br />
Ważna od 18.01.2018, godz. 07:00 CET do 19.01.2018, godz. 07:00 CET</strong></p>
<p><strong>STOPIEŃ 1. ZAGROŻENIA zostaje wydany dla południowo-zachodniej części kraju głównie z powodu ryzyka wystąpienia silnych porywów wiatru pochodzenia konwekcyjnego</strong></p>
<p><strong>SYTUACJA SYNOPTYCZNA:</strong></p>
<p><a href="http://lowcyburz.pl/wp-content/uploads/2018/01/outlook_20180118-2.png"><img class="alignright size-medium wp-image-17586" src="http://lowcyburz.pl/wp-content/uploads/2018/01/outlook_20180118-2-300x239.png" alt="" width="300" height="239" srcset="http://lowcyburz.pl/wp-content/uploads/2018/01/outlook_20180118-2-300x239.png 300w, http://lowcyburz.pl/wp-content/uploads/2018/01/outlook_20180118-2.png 597w" sizes="(max-width: 300px) 100vw, 300px" /></a>Nad Europą Zachodnią i Środkową uaktywniła się strefowa cyrkulacja mas powietrznych. Nad rejonem Islandii znajduje się rozległy niż kształtujący pogodę także w północnej połowie Europy. Na południe od tego ośrodka niżowego rozwinął się i dość szybko się przemieszcza na wschód niż wtórny. W czwartek i w nocy z czwartku na piątek nad Europą Środkową będzie przebiegać smuga silnego górno-troposferycznego prądu strumieniowego. Ponadto w związku z wędrówką niżu wtórnego, w dolnej troposferze wzrośnie gradient baryczny, co przyczyni się do występowania silnego wiatru także przy powierzchni ziemi. Ośrodek niżu wtórnego w nocy z czwartku na piątek znajdzie się nad środkową częścią Polski, po czym odejdzie na północny wschód. Za niżem i jego systemem frontów atmosferycznych napłynie chłodne i bardziej chwiejne powietrze polarne morskie.</p>
<p><strong>OMÓWIENIE:</strong></p>
<p><strong>&#8230; południowy zachód Polski &#8230;</strong></p>
<p>Popołudniem i pod wieczór front chłodny przechodzący w front zokludowany przejdzie przez zachód i południe kraju. W jego rejonie pojawi się niewielka energia CAPE, jednak w przypadku dynamicznego niżu wtórnego w zupełności wystarczająca do rozwinięcia się wyżej wypiętrzonych układów komórek konwekcyjnych. Silne quasigeostroficzne wymuszanie wznoszenia w rejonie strefy frontowej może doprowadzić do powstania kilku ośrodków burzowych. Burze te będą organizować się w układy liniowe. Możliwy jest zarówno jeden lub dwa większe liniowe układy konwekcyjne, jak i kilka mniejszych. Podczas ich przechodzenia będzie występować silny i porywisty wiatr (prędkość wiatru do 32-36 m/s na poziomie izobarycznym 850 hPa!). W kilku przypadkach mogą pojawić się niszczące porywy wiatru o sile F1, stąd wydanie stopnia 1. zagrożenia jest całkowicie uzasadnione.</p>
