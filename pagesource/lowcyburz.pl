			<h2 class="entry-title"><a href="http://lowcyburz.pl/2018/01/14/prognoza-pogody-na-kolejne-dni/" title="Bezpośredni odnośnik do Prognoza pogody na kolejne dni" rel="bookmark">Prognoza pogody na kolejne dni</a></h2>

			<div class="entry-meta">
				<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="http://lowcyburz.pl/2018/01/14/prognoza-pogody-na-kolejne-dni/" title="16:17" rel="bookmark"><span class="entry-date">14 stycznia 2018</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="http://lowcyburz.pl/author/igor-laskowski/" title="Zobacz wszystkie wpisy, których autorem jest Igor Laskowski">Igor Laskowski</a></span>			</div><!-- .entry-meta -->

				<div class="entry-content">
				<p><strong>Po okresie względnej stabilizacji pogody jaką zapewnia nam układ wysokiego ciśnienia o centrum ulokowanym nad zachodnią Rosją, kolejny tydzień przyniesie nam zwiększenie dynamiki w pogodzie. Do naszego kraju zacznie zbliżać się rozległa zatoka niskiego ciśnienia znad Atlantyku, na obrzeżach której będą tworzyć się niże przynoszące opady – przeważnie śniegu, lokalnie na zachodzie deszczu ze śniegiem i deszczu, okresami będzie również dość wietrznie, a na drogach może być ślisko.</strong></p>
<p>Dzisiejszy poranek był dość mroźny w wielu regionach Polski. Najchłodniej było na północnym-wschodzie oraz na Pomorzu Gdańskim, gdzie temperatura powietrza spadła lokalnie do -7°C &#8211; -8°C. Im dalej na zachód tym cieplej – tam temperatura powietrza rano wyniosła -2°C &#8211; -3°C. Jednocześnie jest dość pochmurno – poza niektórymi regionami centralnej i południowej części Polski, utrzymuje się niskie zachmurzenie kłębiasto-warstwowe w postaci chmur Stratocumulus. Nadchodząca noc będzie dość zbliżona do dzisiejszej, najchłodniej będzie na wschodzie, na południowym-wschodzie oraz w regionach podgórskich gdzie temperatura powietrza spadnie do -8°C &#8211; -11°C. Lokalnie na obszarach tzw. mrozowisk może być jeszcze chłodniej. Najcieplej będzie natomiast na zachodzie Polski, do -1°C &#8211; -2°C.</p>
