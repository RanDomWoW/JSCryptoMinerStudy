			<h2 class="entry-title"><a href="http://lowcyburz.pl/2018/01/17/prognoza-konwekcyjna-na-dzien-17-01-2018-i-noc-17-18-01-2018/" title="Bezpośredni odnośnik do Prognoza konwekcyjna na dzień 17.01.2018 i noc 17/18.01.2018" rel="bookmark">Prognoza konwekcyjna na dzień 17.01.2018 i noc 17/18.01.2018</a></h2>

			<div class="entry-meta">
				<span class="meta-prep meta-prep-author">Opublikowano</span> <a href="http://lowcyburz.pl/2018/01/17/prognoza-konwekcyjna-na-dzien-17-01-2018-i-noc-17-18-01-2018/" title="00:44" rel="bookmark"><span class="entry-date">17 stycznia 2018</span></a>, <span class="meta-sep">autor:</span> <span class="author vcard"><a class="url fn n" href="http://lowcyburz.pl/author/krzysztof_piasecki/" title="Zobacz wszystkie wpisy, których autorem jest Krzysztof Piasecki">Krzysztof Piasecki</a></span>			</div><!-- .entry-meta -->

				<div class="entry-content">
				<p><strong>PROGNOZA SIŁY I AKTYWNOŚCI BURZ DLA POLSKI<br />
Ważna od 17.01.2018 godz. 07:00 CET do 18.01.2018, godz. 07:00 CET</p>
<p>SYTUACJA SYNOPTYCZNA/OMÓWIENIE:</strong></p>
<p><a href="http://lowcyburz.pl/wp-content/uploads/2018/01/prognoza.png"><img class="wp-image-17578 alignleft" src="http://lowcyburz.pl/wp-content/uploads/2018/01/prognoza.png" alt="" width="396" height="315" srcset="http://lowcyburz.pl/wp-content/uploads/2018/01/prognoza.png 597w, http://lowcyburz.pl/wp-content/uploads/2018/01/prognoza-300x239.png 300w" sizes="(max-width: 396px) 100vw, 396px" /></a>W środę przeważająca część Polski znajdzie się w zasięgu oddziaływania zatoki rozległego ośrodka niskiego ciśnienia z centrum ulokowanym w rejonie Islandii. W obrębie osi zatoki skierowanej na południowy-wschód, będą tworzyć się kolejne dolne ośrodki niskiego ciśnienia, sprzyjające powstawaniu ruchów wznoszących mas powietrza oraz opadów – w przeważającej części śniegu. Do Polski z kierunków zachodnich i północno-zachodnich będzie napływać masa powietrza polarnego morskiego, które będzie wypierać chłodniejszą masę powietrza kontynentalnego zalegającą głównie we wschodniej części kraju.</p>
<p>Napływająca masa powietrza będzie charakteryzować się bardzo niską temperaturą powietrza w środkowej troposferze (ok. -38°C &#8211; -40°C na wysokości izobarycznej 500 hPa), która spowoduje znaczne zaostrzenie pionowego gradientu temperatury w tej części troposfery, do ok. 8,5-9°C/km. Taki gradient sprzyjać będzie pojawieniu się niewielkiej ilości energii potencjalnej dostępnej drogą konwekcji, sprzyjającej powstawaniu burz adwekcyjnych. Chwiejność może osiągnąć do około 100 J/kg, zaś wskaźnik LI do -1°C. Będzie to sprzyjać rozwojowi chmur kłębiastych, które będą przynosić przelotne, ale dość intensywne opady deszczu ze śniegiem i śniegu, a miejscami krupy i drobnego gradu. Porywy wiatru w czasie ich przechodzenia mogą wynieść 50-75 km/h. Nie można wykluczyć, że bardziej wypiętrzone komórki przyniosą wyładowania atmosferyczne, które na ogół nie będą jednak liczne. Podwyższone ryzyko takich zdarzeń wystąpi na obszarze objętym prognozą, jednak również na pozostałym terenie istnieje marginalne ryzyko pojawienia się słabych burz. Największe szanse istnieją na samym wybrzeżu oraz nad wodami Bałtyku.</p>
