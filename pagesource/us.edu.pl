                      <img  class="imagefield imagefield-field_image" width="103" height="96" alt="logo Lauru Studenckiego" src="http://www.us.edu.pl/sites/www.us.edu.pl/files/laur_logo_4_2.jpg?1519288147" />                  <h3><a href="/xi-gala-wreczenia-nagrod-srodowiskowych-laur-studencki">XI gala wręczenia nagród środowiskowych Laur Studencki</a></h3>                  <p>Laury Studenckie to nagrody przyznawane przez studentów Uniwersytetu Śląskiego od 2007 roku. Są wyrazem podziękowania dla ludzi, organizacji oraz instytucji, które w znacznym stopniu przyczyniły się do rozwoju i upowszechnienia kultury akademickiej. Tegoroczna gala wręczenia nagród środowiskowych odbędzie się w czwartek 22 marca 2018 r. o godz. 18.00 w Teatrze Zagłębia w Sosnowcu. W wydarzeniu udział weźmie&nbsp;prorektor ds. kształcenia i studentów UŚ <a href="http://pracownik.us.edu.pl/us-addressbook/emp/13002">prof. dr hab. Ryszard Koziołek</a>.</p>

<p>Laury Studenckie wręczone zostaną w następujących kategoriach: przyjaciel studenta, przyjazny dziekanat, promotor kultury studenckiej, organizacja przyjazna studentom, instytucja przyjazna studentom oraz nagroda honorowa.                  <div class="more more-link"><a href="/xi-gala-wreczenia-nagrod-srodowiskowych-laur-studencki">czytaj całość<span class="hide"> o wydarzeniu XI gala wręczenia nagród środowiskowych Laur Studencki </span></a></div>  </div>
