<h1>Kiedy postawić przecinek?</h1>

<p>Ile razy się nad tym zastanawialiśmy...</p>

<p>Dlatego powstał ten serwis. Zawiera on, przedstawione prosto i&nbsp;przystępnie, najważniejsze zasady stawiania przecinków, pozwalające na poprawne posługiwanie się tymi znakami w przeważającej większości przypadków.</p>

