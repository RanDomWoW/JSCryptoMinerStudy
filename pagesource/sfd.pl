                                <p>Kreatyna uznawana jest za najskuteczniejsze wsparcie suplementacyjne w zakresie budowania masy mięśniowej oraz poprawy siły mięśniowej. Kreatyna działa anaboliczne oraz zwiększa absorpcje glikogenu, wpływa też pozytywnie na r&oacute;wnowagę kwasowo-zasadową. Podobnie, pozytywnie na nasze ph działa beta alanina. Suplementacja beta alaniną wpływa na og&oacute;lnie pojęty rozw&oacute;j tkanki mięśniowej. Wspiera znacząco naszą silę i wytrzymałość, hamuje też procesy starzenia się. Badania jednoznacznie wykazały, ze połączanie kreatyny z beta alaniną daje znacznie lepsze efekty niż suplementacja sama kreatyną. Zalecana dawka <a href="https://www.sfd.pl/sklep/Kreatyny-k112.html">kreatyny</a> dla budowania masy mięśniowej to 5-10g na dobę. Zalecana dawka beta alaniny to 6-8g na dobę. Oba suplementy można przyjmować w jednej porcji.</p>
