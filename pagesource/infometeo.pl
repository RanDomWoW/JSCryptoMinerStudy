    <h2 class="cities-h2">Prognoza dla Twojego miasta</h2>
    <p class="text-justify">InfoMeteo agreguje <b>najdokładniejsze prognozy</b> i ostrzeżenia z <b>możliwie najlepszych</b> źródeł. Baza miejscowości, którą tutaj widzisz powstawała
