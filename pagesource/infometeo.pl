    Wszak spis miejscowości można znaleźć w innych miejscach… ;-)</p>
    <h2 class="cities-h2">Miejscowości obok siebie</h2>
    <p class="text-justify">Przy poszukiwaniu aktualnej prognozy dla swojej miejscowości, warto wziąć pod uwagę charakterystykę modeli prognostycznych. 
    Siatka obliczeń najdokładniejszego modelu - UM (prognoza 60h) zawiera w sobie obszar kwadratu o boku 26x26km.
    Istnieje zatem bardzo duże prawdopodobieństwo, że dwie miejscowości oddalone od siebie o mniejszą niż 26km odległość, będą przedstawiały tę samą prognozę.
    Nie zawsze zatem jest sens dublować wpisy, chyba że wynika to z różnicy w pozostałych prognozach.</p>
