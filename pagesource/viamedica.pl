    vmpopup.init = function (popupNodeId, url, imgurl, width, height) {
        vmpopup.nodeId = popupNodeId;
        vmpopup.url = url;
        vmpopup.imgurl = '/' + imgurl;
        vmpopup.width = width;
        vmpopup.height = height;
        
        if (getCookie('popup' + popupNodeId)) {
            return false;
        }
        vmpopup.showCurtain(vmpopup.showPopup);
        setCookie('popup' + popupNodeId, '1');
        
        function setCookie (name,value,expires,path,theDomain,secure) { 
           value = escape(value);
           var theCookie = name + "=" + value + 
           ((expires)    ? "; expires=" + expires.toGMTString() : "") + 
           ((path)       ? "; path="    + path   : "") + 
           ((theDomain)  ? "; domain="  + theDomain : "") + 
           ((secure)     ? "; secure"            : ""); 
           document.cookie = theCookie;
        } 
        function getCookie(Name) { 
           var search = Name + "=" 
           if (document.cookie.length > 0) { // if there are any cookies 
              var offset = document.cookie.indexOf(search) 
              if (offset != -1) { // if cookie exists 
                 offset += search.length 
                 // set index of beginning of value 
                 var end = document.cookie.indexOf(";", offset) 
                 // set index of end of cookie value 
                 if (end == -1) end = document.cookie.length 
                 return unescape(document.cookie.substring(offset, end)) 
              } 
           } 
        } 
        function delCookie(name,path,domain) {
           if (getCookie(name)) document.cookie = name + "=" +
              ((path)   ? ";path="   + path   : "") +
              ((domain) ? ";domain=" + domain : "") +
              ";expires=Thu, 01-Jan-70 00:00:01 GMT";
        }
    };
    vmpopup.showCurtain = function (successFunction) {
        setTimeout(function () {
            vmpopup.$curtain = $('<div class="curtain" style="display: none; z-index: 100000; position: fixed; left: 0px; top: 0px; width: 100%; height: 100%; background-color: #fff; opacity: 0.8"></div>').appendTo($('body')).fadeIn(500, successFunction);
        }, 1000);
    };
    vmpopup.showPopup = function () {
        vmpopup.$container = $('<div class="popup" style="z-index: 100001; position: fixed; width: 100%; height: 100%; left: 0px; top: 0px;">').appendTo($('body'));
        var $href = vmpopup.url ? $('<a href="' + vmpopup.url + '"></a>').appendTo(vmpopup.$container) : vmpopup.$container;
        var img = new Image();
        img.onload = function() {
            var $img = $('<img src="' + vmpopup.imgurl + '" style="position: absolute; top: 50%; left: 50%; width: 100%; max-width: ' + vmpopup.width + 'px; height: auto" />').appendTo($href);
            var closerUrl = "/extension/selfstart/design/self/images/iks.png";
            var $closer = $('<img style="cursor: pointer; display: block; position: absolute" src="' + closerUrl + '" />').appendTo(vmpopup.$container);
            $closer.click(vmpopup.close);
            vmpopup.setPositions($img, $closer);
            $(window).resize(function () {
                vmpopup.setPositions($img, $closer);
            });
        };
        img.src = vmpopup.imgurl;
    };
    vmpopup.setPositions = function ($img, $closer) {
        $img.css("margin-left", "-" + ($img.width() / 2) + 'px');
        $img.css("margin-top", "-" + ($img.height() / 2) + 'px');
        var imgPosition = $img.position();
        var closerPosition = {
            left: imgPosition.left + ($img.width() / 2) - 60,
            top: imgPosition.top - ($img.height() / 2) + 20
        };
        $closer.css('left', closerPosition.left + 'px');
        $closer.css('top', closerPosition.top + 'px');
    };
    vmpopup.close = function () {
        vmpopup.$curtain.remove();
        vmpopup.$container.remove();
    };

    
