		var popupCookie = getPopupCookie(prefix+id.toString());
	
		if(popupCookie == '') {
			var popupId = '#'+prefix+id;
			var timeToShow = popups[index].timeToShow * 1000;
			var period = popups[index].period;
			setTimeout( function(popups) {
				$(popupId).modal('show');
			}, timeToShow);
			if(popups[index].hideOnBodyClick) hideOnBodyClick(popupId);
			if(period != 0) setPopupCookie(prefix+id, period);
			index++;
		}
		else {
			index++;
			showNext();
		}
		
	};
	
	var setPopupCookie = function(id, expireDays) {
		
		var date = new Date();
		date.setTime(date.getTime() + (expireDays*24*60*60*1000));
		document.cookie= id+"=true"+"; expires="+date.toUTCString();
	};
	
	var getPopupCookie = function(cname) {
		var name = cname + "=";
	    var ca = document.cookie.split(';');
	    for(var i=0; i<ca.length; i++) {
	        var c = ca[i];
	        while (c.charAt(0)==' ') c = c.substring(1);
	        if (c.indexOf(name) == 0) return c.substring(name.length,c.length);
	    }
	    return "";
	};
	
	var hideOnBodyClick = function(popupId) {
		$(popupId).on('click', function (e) {
			$(popupId).modal('hide');
		});
	}

