            </em>
</a>        </div>
    </div>
    <div class="col-md-9 no-padding">
     <p>
		Biuro Projektów ARCHON+ od przeszło 27 lat projektuje domy dla polskich rodzin. Spod ręki naszych architektów i specjalistów wyszło tysiące autorskich projektów, które zdobią polski krajobraz. Pracownia projektowa ARCHON+ przede wszystkim stawia na jakość. Dzięki temu wyrobiliśmy sobie markę firmy solidnej i godnej zaufania. Mogą Państwo być pewni, że nasze projekty jak i dokumentacja techniczna spełnia najwyższe standardy oraz oczekiwania najbardziej wymagających Inwestorów. Dzięki temu Klienci mają gwarancję spokojnej i płynnej realizacji inwestycji. 
		</p>

		<p>
	
Oferta projektowa ARCHON+ to bardzo bogata kolekcja ponad 1500 różnorodnych projektów domów, wśród której znajdziecie Państwo domy nowoczesne i tradycyjne, domy jednorodzinne oraz wielorodzinne, małe i tanie w budowie, a także wille i rezydencje. Projekty domów ARCHON+ charakteryzują się funkcjonalnością, niebanalną bryłą a przede wszystkim racjonalnym zaprojektowaniem każdej przestrzeni i detalu. Dzięki temu koszty realizacji są optymalne a stosunek ceny do jakości zbilansowany.
</p>
    </div>



<p>
	
Równie ogromną wagę przywiązujemy do jakości obsługi. Mamy świadomość, że decyzja o budowie domu to dopiero wstęp do przygody życia i chcemy zrobić wszystko, aby Państwu w tym pomóc. Ogromnie zależy nam, aby skorzystali Państwo z naszego doświadczenia. Służymy poradą naszych specjalistów na każdym etapie budowy domu marzeń. Począwszy od wyboru najlepszego projektu, poprzez sprawy formalne związane z załatwianiem pozwoleń, po samą budowę, dobór odpowiednich materiałów oraz aranżację wnętrz. 
</p>
<div class="headline  margin-top-20">
<h2>Zrealizuj marzenia o własnym domu!</h2>
</div>
<p>
Z myślą o Państwa komforcie i wygodzie przygotowaliśmy kilka narzędzi, które ułatwią Państwu podjęcie decyzji o wyborze idealnego projektu domu ARCHON+. Na etapie personalizowania konkretnego projektu mają Państwo do dyspozycji takie narzędzia jak: aplikacja energooszczędna, kolornik rzuty interaktywne czy możliwość wirtualnego usytuowania domu na działce. Dla wybranych projektów domów przygotowaliśmy wizualizacje wnętrz oraz wirtualne spacery, aby pomóc Państwu łatwiej wyobrazić sobie i zaplanować własną aranżację pomieszczeń przyszłego domu. 
</p>
<p>
	
Na stronie posiadamy również bogate galerie z filmami i zdjęciami z realizacji, które pozwalają zobaczyć wybudowany dom według naszego projektu. Liczymy na to, że nasze projekty domów przypadną Państwu do gustu. Chcielibyśmy, aby znaleźli Państwo dom, w którym zakocha się cała Wasza rodzina. 

</p>
<p>
	
Państwa marzenia są dla nas inspiracją do tworzenia jeszcze lepszych projektów, w oparciu o najważniejsze potrzeby dotyczące funkcjonalności, stylistyki, wielkości i kształtu domu oraz dostosowania do działki. Zapraszamy do skorzystania z pomocy Doradców projektowych, którzy pomogą Państwu wybrać dom marzeń.
</p>
