<div class='advantage'>
<h3>Wszystkie rachunki w jednym miejscu</h3>
<p>Nie musisz już logować się do każdego banku z osobna.  Kontomierz skupia się na analizie wydatków i przychodów, niezależnie od tego, którego rachunku dotyczą.</p>
</div>
<div class='advantage even'>
<h3>Spersonalizowane rekomendacje</h3>
<p>Natychmiast dowiesz się, ile pieniędzy zyskałbyś rocznie, gdyby Twoje oszczędności leżakowały na korzystniej oprocentowanych rachunkach.</p>
</div>
<div class='advantage'>
<h3>Prowadzenie budżetu domowego</h3>
<p>Zdefiniuj budżet na dowolną kategorię wydatków (np. na rachunki, samochód czy prezenty). Kontomierz będzie śledził wykorzystania budżetu i poinformuje Cię, gdy go przekroczysz.</p>
</div>
<div class='advantage even'>
<h3>Automatyczny import danych</h3>
<p>Kontomierz jest jedyną aplikacją webową oferującą całkowicie automatyczny import rachunków i transakcji bankowych.</p>
