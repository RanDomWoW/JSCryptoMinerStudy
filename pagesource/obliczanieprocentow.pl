        <p>1. zamienić <em>procent</em> na ułamek<br />
          2. 
          wartość liczbową podanej liczby % przemnożyć przez odwrotność uzyskanego ułamka</p>
        <p>Wiemy że:</p>
        <p><em>100 procent</em> to całość<br />
          50 <em>procent</em> to połowa<br />
          25 <em>procent</em> to ćwierć<br />
          10 <em>procent</em> to jedna dziesiąta<br />
          20 <em>procent</em> to jedna piąta </p>
        <p>Przykład: <br />
          Znajdź liczbę, której 50% jest równe 16.</p>
        <p>Wiemy, że 50% to połowa. Czyli mamy znaleźć liczbę, której połowa jest równa 16.<br />
