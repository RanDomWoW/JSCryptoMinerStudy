      <h2><a name="obliczanieprocentów" id="obliczanieprocentów"></a><strong>1. Definicja Procentu</strong></h2>
    </div>
    <div class="tresc">
      <p><strong>Procent</strong> (od łac. <em>per   centum</em>, &bdquo;przez sto&rdquo;; od <em>per</em> – poprzez, przez, za pomocą; <em>centum</em> – sto) – w matematyce sposób wyrażenia liczby jako ułamka <br />
      o mianowniku 100, zwykle   oznaczany symbolem %, np. <span dir="ltr">45%</span> (czyt. &bdquo;czterdzieści pięć <em>procent</em>&rdquo;) oznacza <img alt="oblicz procent" src="Grafika/procent-b556bae3db2d0f51028c03d735689676.png" /> lub <span dir="ltr">0,45.</span></p>
      <p><strong>Procenty</strong> umożliwiają wygodne wyrażenie danej wielkości w stosunku do innej,   przy czym pierwsza wielkość oznacza zwykle część lub zmianę
        w drugiej.   Przykładowo wzrost ceny 2,50 zł o 0,15 zł oznacza jej wzrost wyrażony ułamkiem <img alt="procent" src="Grafika/procent-112d535dfe786fdbfdd5d73ccffc92ff.png" /><br /> 
        Innymi słowy cena wzrosła o <span dir="ltr">6%.</span>    <br />
      Popularnie <em>procentem</em> nazywa się również ułamek <img alt="\tfrac{1}{100}," src="Grafika/procent-Untitled-1.png" /> czyli 1%. Jeden <em>procent</em> dzieli się na 10 promili.</p>
</div>
