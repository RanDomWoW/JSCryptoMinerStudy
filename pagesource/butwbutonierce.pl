<p>But w Butonierce BSC to stowarzyszenie, które skupia miłośników klasycznej elegancji. Promujemy nie tylko elegancki wizerunek, lecz także postawę i nastawienie wobec życia – etykietę i savoir vivre.</p>
<p><img style="margin: 10px; padding: 6px; border: 1px solid #6e6e6e; float: left;" src="/images/BwB.jpg" alt="BwB" width="550" height="367" />Wspieramy rzemieślników oraz sprzedawców towarów i usług, dla których jakość i zadowolenie klienta są priorytetem. Żyjąc w czasach masowej produkcji, staramy się odkrywać tych niewielu pozostałych, tajemniczych mistrzów interesujących nas profesji związanych z elegancją i stylem życia.</p>
<p>Cenimy zarówno kunszt włoskich mistrzów igły, jak i krawców z londyńskiej Savile Row. Podziwiamy niszowe destylarnie szkockiej single malt whisky, dyskutujemy o jakości butów ze starego i nowego świata, ale najbardziej na sercu leży nam odrodzenie świetności polskich rzemieślników, którzy przed laty również słynęli na świecie. Dziś tych wybitnych została garstka. Niektórzy doskonale odnaleźli się w nowych realiach, inni zaś potrzebują w tym wsparcia. Jedni i drudzy zasługują na naszą uwagę.</p>
<p>Nasze stowarzyszenie prowadzi największe w Polsce forum dyskusyjne o klasycznej męskiej elegancji, którego zasoby są do dyspozycji wszystkich. Promujemy eleganckie życie, rozrywki i spędzanie wolnego czasu. Od sportów, rekreacji i hobbies po jedzenie, używki czy gadżety. Tych, którzy lubią cieszyć się życiem na wysokim poziomie, z pożytkiem dla siebie i szacunkiem dla innych, zapraszamy na imprezy organizowane przez Stowarzyszenie.</p> 
	
