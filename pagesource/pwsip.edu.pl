	<h1 style="text-align: center;">3 miejsce w rankingu "Perspektyw"</h1>
<p style="text-align: center;">Państwowa Wyższa Szkoła Informatyki i Przedsiębiorczości w Łomży uplasowała się w 2017 roku na trzecim miejscu rankingu portalu edukacyjnego "Perspektywy". Wskaźnik jaki osiągnęła łomżyńska „Państwówka” to 84.1, czyli zaledwie o 0.4 punktu mniej od uczelni, która zajęła drugie miejsce w prestiżowym zestawieniu w kategorii Państwowych Wyższych Szkół Zawodowych.<br /> Trzecie miejsce to awans PWSIiP o dwie pozycje względem ubiegłego roku. Uczelnia osiągnęła wysoki, drugi w rankingu, wskaźnik "siły naukowej" (92.20). To rozbudowana i najważniejsza kategoria, stanowiąca 35 proc. wagi całego rankingu. Składają się na nią dane na temat kierunków studiów i oferty kształcenia podyplomowego, kadry naukowej i jej rozwoju, publikacji oraz tzw. cytowań, ocen parametrycznych poszczególnych części składowych uczelni. PWSIiP wysoko uplasowała się także w kategoriach „absolwenci na rynku pracy” oraz „warunki kształcenia”.</p>
