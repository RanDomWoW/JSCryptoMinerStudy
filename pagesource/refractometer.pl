</script></div>  <p>The refractometer is a relatively simple device for measuring the angle of refraction at the liquid-solid phase interface. The angle of refraction depends on the composition of the solution, which allows the use of refractometers for quick evaluation of concentration of dissolved substances. They can be also used, although to a lesser extent, to study the structure of chemical compounds.</p>
<p>There are many refractometer designs, differing in the attainable accuracy, ease of use and applicability for different types of measurements of reactive indices (solids, liquids, gases). Two most popular types, used for measurement of refractive index of liquids, are Abb&eacute; and Pulfrich refractometers. However, while at the end of the 19<sup>th</sup> century and at the beginning of 20<sup>th</sup> century both names were assigned to refractometers produced by Carl Zeiss Jena, after WWI similar models became available from other producers. Each model was modified in many ways, so at present names Abb&eacute; refractometer and Pulfrich refractometer are not precisely describing device, and in some cases classification of the refractometer as "Abb&eacute;" or "Pulfrich" can be misleading.</p>
<p>There are several principles used in refractometers. Most common are critical angle refractometers, in which we measure position of the border between a bright and dark. There are refractometers in which we also measure position of the border between bright and the dark, but the border is not created thanks to existence of critical angle, but is produced by the cuvette shape. There are also interferometric refractometers, in which interferometry is used to measure difference in speeds of the light in diferent media. Contrary to the name (interferometric refractometer) they do not refract light to make a measurement (although they can use refraction to guide the light beam).</p>  <hr/>
