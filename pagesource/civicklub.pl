			<a href="http://null/index.php/2016/10/05/honda-civic-type-r-2018-swiat-ujrzal-prototyp/" rel="bookmark" title="Honda Civic Type R 2018 &#8211; Świat ujrzał prototyp!">Honda Civic Type R 2018 &#8211; Świat ujrzał prototyp!</a>
		</h2><!--/.post-title-->

		      <div class="entry excerpt">
        <p>&nbsp;</p>
<p>Tym razem nie musieliśmy czekać tak długo jak ostatnio. Na trwającym właśnie salonie samochodowy w Paryżu odbyła się oficjalna premiera X-tej generacji popularnego Civic’a. Dla posiadających nieco bardziej wysmakowane gusta miłośników marki, producent pokazał także prototyp topowej wersji &#8211; Type R. Premierę wersji fabrycznej zaplanowano na rok 2017.</p>
<p>Samochód porównując do obecnego modelu Type R, opartego na IX-tej generacji Civic’a mocno się zmienił. Bazą było nadwozie seryjnego modelu X-tej generacji, którego sylwetka auta mocno odbiega od sylwetki poprzednika. Na pierwszy rzut oka od razu widać, że auto zmieniło proporcje. Stało się bardziej przysadziste, zwarte. Nie ma tu mowy o kolejnym „mocnym liftingu” jak to miało miejsce przy prezentacji IX-tej generacji, która nie wiele zmieniła się od popularnej „ósemki”.</p>
<p>Wyraźnie wydłużyła się maska. Kompletnie inaczej zaprojektowano przód auta, który stracił wyrazisty grill. Nie mogło jednak zabraknąć kultowego w tej odmianie znaczka z czerwonym tłem, oraz plakietki „Type R”. Duże wloty powietrza w zderzaku odpowiadać będą zapewne za doprowadzenie to intercooler’a odpowiedniej ilości powietrza. Boczne wloty usytuowane tuż za światłami przeciwmgielnymi znalazły się tam aby z kolei skutecznie chłodzić przednie hamulce. Boczne błotniki także „napompowano” tworząc w nich (podobnie jak w obecnym modelu) duże wloty powietrza. Jak zapewnia producent tak mocno rozbudowany pakiet stylistyczny nie służy wyłącznie przyciąganiu uwagi przechodniów, gdy auto znajdzie się już na ulicy. Każdy, nawet pojedynczy element spełnia swoją określoną funkcję aerodynamiczne.<br />
Aby koła nie wyglądały karykaturalnie, zdecydowano się na montaż 20 calowych felg, na które naciągnięto opony o szerokości aż 245 mm! Dużo, jak na auto kompaktowe.<br />
Tył auta także mocno się zmienił. Ogromny dyfuzor mieści centralnie 3 duże końcówki układu wydechowego. Masywnie przetłoczony zderzak, analogicznie do przedniego posiada duże wloty powietrza. Dużo większe niż w „dziewiątce” lampy, nadal tworzą kształt litery C. Nad nimi góruje już tylko ogromny spoiler, którego zadaniem będzie zapewnienie odpowiedniej siły docisku na tylnej części auta przy wyższych prędkościach.</p>
<p>Na tą chwilę producent nie ujawnia zbyt wiele jeśli chodzi o rozwiązania mechaniczne. Można domniemywać, że nowy Type R, w drodze ewolucji, otrzyma także 2-litrowych, turbodoładowany motor generujący co najmniej 310 KM i 400 Nm. Nam, fanom tej marki i tego modelu pozostaje wierzyć, że moc generowana przez jednostkę napędową jeszcze wzrośnie (w odniesieniu do obecnej wersji) a wrażenia z jazdy będą co najmniej tak dobre jak w „dziewiątce”. Tego jak finalnie auto wygląda, jeździ i brzmi dowiemy się dopiero w 2018 roku.</p>
