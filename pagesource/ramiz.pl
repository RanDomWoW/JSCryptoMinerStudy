function resize_images_tiles(imgclass, containerclass, imgcontainerclass, contentclass, priceclass)
{
	$(imgclass).imagesLoaded(function(img) {
		var perline = 3;
		var imgs = {}, conts = {}, price = {}, i = 0, j = 0, max_img_height = 0, max_content = 0;
		$('[name="'+containerclass+'"]').each(function(){
			if ( i == perline )
			{
				i = 1;
				j++;
			}
			else i++;

			$(this).find(imgcontainerclass).each(function(){
				if ( typeof imgs[j] == 'undefined' || imgs[j] < $(this).height() )
					imgs[j] = $(this).height();
			});

			$(this).find(contentclass).each(function(){
				if ( typeof conts[j] == 'undefined' || conts[j] < $(this).height() )
					conts[j] = $(this).height();
			});
			$(this).find(priceclass).each(function(){
				if ( typeof price[j] == 'undefined' || price[j] < $(this).height() )
					price[j] = $(this).height();
			});
		});
		var j = 0, i = 0;

		$('[name="'+containerclass+'"]').each(function(){
			if ( i == perline )
			{
				i = 1;
				j++;
			}
			else i++;

			$(this).find(imgcontainerclass).height(imgs[j]);
			$(this).find(contentclass).height(conts[j]);
			$(this).find(priceclass).height(price[j]);
		});
	});
