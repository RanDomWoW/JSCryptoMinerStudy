        //Get size of the image, how many images there are, then determin the size of the slider.
        var sliderWidth = $(".window").width();
        var divSum = $(".slider div.slide").size();
        var imageReelWidth = sliderWidth * divSum;

        //Adjust the slider to its new size
        $(".slider").css({'width': imageReelWidth + 160});
        if ($('.slider .slide').length <= 1) {
            $("div.pagination").hide();
        }

        //Paging  and Slider Function
        rotate = function () {
            //alert($active.attr("rel"));
            var triggerID = $active.attr("rel") - 1; //Get number of times to slide
            var image_reelPosition = triggerID * sliderWidth; //Determines the distance the image reel needs to slide
            var flag = false;
            $(".paging a").removeClass('active'); //Remove all active class
            //$active.addClass('active'); //Add active class (the $active is declared in the rotateSwitch function)

            $(".paging a[rel=" + Number($active.attr("rel")) + "]").addClass('active');

            nxtRelVal = Number($active.attr("rel")) + 1;
            if (nxtRelVal == divSum + 1) {
                nxtRelVal = 1;
            }

            prvRelVal = Number($active.attr("rel")) - 1;
            if (prvRelVal < 1) {
                prvRelVal = divSum;
            }

            $('.next').attr("rel", nxtRelVal);
            $('.previous').attr("rel", prvRelVal);

            //Slider Animation
            if (triggerID == 0) {
                $(".slider").animate({
                    left: -image_reelPosition
                }, 500);
            } else {
                $(".slider").animate({
                    left: -image_reelPosition
                }, 500);
            }
        };

        //Rotation  and Timing Event
        rotateSwitch = function () {
            play = setInterval(function () { //Set timer - this will repeat itself every 7 seconds
                $active = $('.paging a.active').next(); //Move to the next paging
                if ($active.length === 0) { //If paging reaches the end...
                    $active = $('.paging a:first'); //go back to first
                }
                rotate(); //Trigger the paging and slider function
            }, 7000); //Timer speed in milliseconds (7 seconds)
        };

        rotateSwitch(); //Run function on launch

        //On Hover
        $(".slider a").hover(function () {
            clearInterval(play); //Stop the rotation
        }, function () {
            rotateSwitch(); //Resume rotation timer
        });

        //On Click
        $(".paging a").click(function () {
            $active = $(this); //Activate the clicked paging

            //Reset Timer
            clearInterval(play); //Stop the rotation
            rotate(); //Trigger rotation immediately
            rotateSwitch(); // Resume rotation timer

            return false; //Prevent browser jump to link anchor
        });

        //On Click
        $(".next").click(function () {
            $active = $(this); //Activate the clicked paging
            //Reset Timer
