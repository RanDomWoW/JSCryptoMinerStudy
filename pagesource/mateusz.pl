      <h2><a href="/mt/br/pojednajcie-sie-z-bogiem.htm">Pojednajcie się z Bogiem</a></h2> 
      <p>„W imię Chrystusa pojednajcie się z Bogiem”. Kiedy sięgamy do tego tekstu tak jak go napisał Paweł w języku greckim to tam jest to słowo „pojednajcie się” zapisane w trybie biernym (katallagēte) to znaczy bądźcie pojednani albo dajcie się pojednać. Ta strona pasywna jest niesłychanie uderzająca. To nie jest tak, że to my chcemy się pojednać z Bogiem, to nie jest tak, że od nas wychodzi ta inicjatywa. To jest tak, że to Bogu zależy na tym, by człowiek był z Nim pojednany. To jest boża inicjatywa...</a></p>
	
