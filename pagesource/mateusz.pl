      <p>Bóg wybiera ludzi, by na nich budować swój Kościół.  Ewangelia dzisiejsza mówi jakie musi mieć kwalifikacje człowiek, który jest dla Kościoła skałą, który jest dla Kościoła fundamentem, na którym Kościół może być zbudowany. Ciekawe jakich my byśmy się spodziewali cech po takim człowieku? Co musi mieć w sobie człowiek, żeby można go było uczynić skałą na, której buduje się Kościół Jezusa Chrystusa? Jaki to musi być człowiek? Mogłyby padać różne odpowiedzi ale ta ewangelia daje tylko jedną, że ten ktoś musi wiedzieć kim jest Jezus Chrystus...</a></p>
	    
	        
