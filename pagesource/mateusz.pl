      <h2><a href="/mt/br/grzech-droga-do-poznania-boga.htm">Grzech - droga do poznania Boga</a></h2> 
      <p>Kiedy popełniamy grzech ale staniemy w prawdzie w związku ze swoim grzechem, to ten nasz grzech staje się dla nas drogą do poznania Boga, do odkrycia, że Bóg jest. Do doświadczenia kim On jest i to jest doświadczenie, które nas do Niego upodabnia. To jest właśnie 8 rozdział ewangelii św. Jana. Przyprowadzili kobietę schwytaną na cudzołóstwie i to jej cudzołóstwo stało się jej drogą do Jezusa. Ona teraz stoi przed Jezusem, doszło do spotkania. Jezus mówi: Ja Cię nie potępiam, idź, więcej nie grzesz...</a></p>
	  
