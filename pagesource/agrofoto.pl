    // Function based on ipsfocus' methods for photoLayout objects
    function update_container( container )
    {
        var max_width       = container.parent().innerWidth() - 14,
            initial_height  = Math.floor(max_width / 4),
            w_arr           = [], 
            maxrows         = container.attr( 'data-ipsphotolayout-maxrows' ),
            images          = [],
            margin          = 4;

        if( container.attr( 'class' ) !== 'patchwork_hot_images' )
        {
            max_width = container.width();
        } 
      
        // Try to set every image's dimensions
        $( '.cGalleryPatchwork_image', container).each(
            function()
            {
                var w = $( this ).prop( 'naturalWidth' ),
                    h = $( this ).prop( 'naturalHeight' );

                if( h != initial_height )
                {
                    w = Math.floor( w * ( initial_height / h ) );
                }
                
                // Skip the image if it has invalid attributes or does not exist
                if(isNaN( w ) || w === 0)
                {
                    $( this ).parent().parent().addClass( 'broken_item' );
                }
                else
                {
                    w_arr.push( w );
                    images.push( $( this ) );
                }

            }
        );
        
        var images_so_far   = 0,
            row_num         = 0,
            items           = $( '.cGalleryPatchwork_item:not(.broken_item)', container),
            items_length    = images.length;
          
        // Remove contents of the container
        container.html( '' );
        
        while( images_so_far < items_length )
        {
            row_num++;
          
            if( row_num > maxrows )
            {
                break;
            }
            
            var width_of_row    = 0,
                images_in_row   = 0;
          
            // Compute the predicted width of the row
            while( ( width_of_row * 1.1 < max_width ) && ( images_so_far + images_in_row < items_length ) )
            {
                var gap = margin * 2;
                
                if( images_in_row === 0 )
                {
                    gap = margin;
                }
                
                width_of_row += w_arr[ images_so_far + images_in_row++ ] + gap;
            }
          
            width_of_row -= margin;
          
            var ratio               = max_width / width_of_row,
                last_row_not_fit    = false,
                k                   = 0,
                row                 = $( '<div/>' ).addClass( 'cGalleryPatchwork_row ipsClearfix' );
          
            container.append( row );
            width_of_row = 0;

            var row_height = Math.floor( initial_height * ratio );
            row.height( row_height + ( 2 * margin ) );
          
            while( k < images_in_row )
            {
                var item_html   = items[ images_so_far + k ],
                    img         = images[ images_so_far + k ],
                    new_width   = Math.floor( w_arr[ images_so_far + k ] * ratio ),
                    img_w       = img.prop( 'naturalWidth' ), 
                    img_h       = img.prop( 'naturalHeight' );
              
                width_of_row += new_width + ( margin * 2 );
                row.append( item_html );

                // Add the image to the row and set its attributes
                $( '.cGalleryPatchwork_item:eq(' + ( images_so_far + k ) + ')', container )
                .height( row_height )
                .width( new_width )
                .css(
                    {
                        'margin': margin,
                        'margin-left': ( k === 0 ) ? 0 : margin,
                        'margin-right': ( k + 1 === images_in_row ) ? 0 : margin
                    }
                );          
                k++;
            }
          
            width_of_row -= ( 2*margin );
            images_so_far += images_in_row;
          
            // Resize images if there is such a need
            if( !last_row_not_fit )
            {
                var i = 0;

                while ( width_of_row < max_width && i !== undefined )
                {
                    var item = row.find( '.cGalleryPatchwork_item:nth-child( '+ ( i + 1 ) +' )' );
                    item.css( 'width', ( item.width() + 1) + 'px' );
                    i = ( i + 1 ) % images_in_row;
                    width_of_row++;
                }
              
                var j = 0;

                while (width_of_row > max_width && j !== undefined)
                {
                    var item = row.find( '.cGalleryPatchwork_item:nth-child( '+ ( j + 1 ) +' )' );
                    item.css( 'width', ( item.width() - 1 ) + 'px' );
                    j = ( j + 1 ) % images_in_row;
                    width_of_row--;
                }

            }

        }
      
        // Hide the last row if it consists of only one image
        $( '.cGalleryPatchwork_row', container ).each(
            function()
            {
                if( $( '.cGalleryPatchwork_item', this ).length === 1 )
                {
                    $( this ).hide();
                }
            }
        );
    }
      
