<script>

function secondsToDate( s )
{
    var d = new Date( s * 1000 );
    return d;
}

function buildDate( s )
{
    var date    = secondsToDate( s );
    var newDate = date.getDate() + '.' + ( '0' + ( date.getMonth() + 1 ) ).slice( -2 ) + '.' + date.getFullYear();

    return newDate;
}

function showNewsTicker( arr )
{
    var newsTicker  = $( '#agronews_ticker' );
    var wrapper     = $( '<ul/>' ).attr( 'id', 'agronews_list' );

    for( var k = 0; k < arr.length; k++ )
    {
        var dateDiv = $( '<span/>' ).addClass( 'date' );
        var msgDiv  = $( '<span/>' ).addClass( 'message' );
        var item    = $( '<li/>' );

        dateDiv.text( arr[ k ].date + ' ' );
        msgDiv.text( arr[ k ].title + ' ' );

        $( '<a/>' ).attr(
            {
                target: '_blank',
                href:   arr[ k ].url
            }
        )
        .addClass( 'agronews_link' )
        .append( dateDiv, msgDiv )
        .appendTo( item );

        item.appendTo( wrapper );
    }

    newsTicker.append( wrapper );
    wrapper.webTicker();

    $( '.agronews_link' ).click(
        function()
        {
            event.preventDefault();
            var pageURL = $( this ).attr( 'href' );
            ga( 'send',
                {
                    hitType: 'event',
                    eventCategory: 'AgroFoto News',
                    eventAction: 'click',
                    eventLabel: pageURL
                }
            );
            window.open(pageURL, '_blank');
        }

    )
}

function buildNews( arr )
{
    var newsArr = [];

    for( var k = 0; k < arr.length; k++ )
    {
        var newsObj = {
            title:  arr[ k ].title,
            url:    arr[ k ].url,
            date:   buildDate( arr[ k ].last_post )
        };
        newsArr.push( newsObj );
    }

    showNewsTicker( newsArr );
}

$( function()
