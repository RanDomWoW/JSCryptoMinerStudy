<p class="pseudo-h2" style="font-weight: normal; letter-spacing: 3px; font-size: 14pt; color: #00a1e4;"><span style="color: #ff4f26;">NAJWYŻSZA JAKOŚĆ OD 13 LAT</span></p>
<p style="color: #656565; font-size: 11pt; line-height: 1.3; text-align: justify;">Od 13 lat dbamy o Twoje zadowolenie wykonując corocznie miliony odbitek i tysiące usług fotograficznych. Specjalnie dla Ciebie rozwijamy stale naszą ofertę umożliwiając Ci wywołanie zdjęć w dowolnym formacie, przygotowanie fotoksiążki, a także wykonanie prawie nieograniczonej ilości fotoprezent&oacute;w &ndash; kubk&oacute;w, koszulek, poduszek, maskotek, puzzli czy etui na telefon z nadrukiem wykonanym z Twojego zdjęcia. Jesteśmy najpopularniejszym serwisem z odbitkami w Polsce i cenimy sobie Twoją satysfakcję z jakości i szybkości naszych usług, dlatego też każda Twoja uwaga jest dla nas bardzo cenna.</p>
<div class="long-text-hide">
<h3 style="font-weight: normal; color: #00a1e4; margin-bottom: 0;"><span style="color: #ff4f26;"><a href="/odbitki">ODBITKI ZDJĘĆ</a></span></h3>
<p style="color: #656565; font-size: 11pt; line-height: 1.3; margin-top: 0px; text-align: justify;">Zam&oacute;wienie odbitek w empikfoto.pl to gwarancja jakości, satysfakcji i terminowości. Oferujemy najr&oacute;żniejsze rozmiary i rodzaje papier&oacute;w fotograficznych, z kt&oacute;rych możesz dowolnie wybierać składając swoje zlecenie. Nasze materiały gwarantują długowieczność Twoich fotografii bez utraty jakości. Już teraz wywołaj zdjęcia w empikfoto.pl i podaruj sobie ponadczasową pamiątkę, kt&oacute;ra na długie lata zachowa Twoje najcenniejsze wspomnienia. Każdą odbitkę przygotowujemy z należytą starannością, bo Twoje fotografie to Twoje najcenniejsze chwile, do kt&oacute;rych my pomagamy Ci wracać.</p>
<h3 style="font-weight: normal; color: #00a1e4; margin-bottom: 0;"><span style="color: #ff4f26;"><a href="/fotoksiazki">FOTOKSIĄŻKA</a></span></h3>
<p style="color: #656565; font-size: 11pt; line-height: 1.3; margin-top: 0px; text-align: justify;">Fotoksiążka to coś więcej niż album fotograficzny. Już teraz możesz stworzyć trwałą i w pełni spersonalizowaną książkę z Twoimi najcenniejszymi fotografiami. Dzięki intuicyjnemu oprogramowaniu dajemy Ci możliwość zaprojektowania każdej strony Twojej książki &ndash; od dowolnego ułożenia zdjęć, aż po wyb&oacute;r tła, ramki lub dodatkowych element&oacute;w graficznych. Zachowaj swoje zdjęcia z podr&oacute;ży, ślubu, wakacji, urodzin lub dowolnej okazji, kt&oacute;ra była dla Ciebie ważna. Fotoksiążka to także idealny prezent dla bliskiej osoby, kt&oacute;rej chcesz podarować coś szczeg&oacute;lnego. Fotoksiążkę możesz zam&oacute;wić na naszej stronie korzystając z edytora online.</p>
<h3 style="font-weight: normal; color: #00a1e4; margin-bottom: 0;"><span style="color: #ff4f26;">PEŁEN WACHLARZ MOŻLIWOŚCI</span></h3>
<p style="color: #656565; font-size: 11pt; line-height: 1.3; margin-top: 0px; text-align: justify;">W naszej ofercie odnajdziesz także bogatą gamę fotoprezent&oacute;w - kubk&oacute;w, koszulek, poduszek, puzzli czy etui na telefon, na kt&oacute;rych możesz umieścić swoje dowolne zdjęcie. Przygotuj niebywały i bardzo indywidualny prezent dla bliskiej Ci osoby, kt&oacute;rą chcesz zaskoczyć i obdarować czymś naprawdę wyjątkowym. Każdy z fotoprezent&oacute;w możesz przygotować według własnych upodobań wykorzystując do tego gotowe szablony, ramki, tła i dodatkowe elementy graficzne.&nbsp;</p>
