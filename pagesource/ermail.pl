            </div><!-- .header -->
            <div class="article">
              <div class="text">
                <p>eRmail = Chciany e-mail reklamowy</p>
             <p>ERmail jest rewolucyjną <strong>bezpłatną</strong> usługą, która umożliwi Ci zarabianie pieniędzy na tym, co do tej pory robiłeś za darmo - na czytaniu reklam.</p>
