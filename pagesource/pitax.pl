	<p>Niania, swoje dochody, powinna rozliczyć w PIT-36 w wierszu „Inne źródła”. Przychody należe pomniejszyć o koszty uzyskania przychodu, czyli o 20% (obliczamy je od przychodu pomniejszonego o zapłacone przez rodzica lub siebie składki na ubezpieczenie). Tak wypełniony PIT, urząd skarbowy online może przyjąć lub rozliczenie roczne niania można złożyć osobiście do urzędu.</p>
	<br/></a>
</li></ul><ul id="three"><li>
	<a href="/wiedza/sytuacje-podatkowe/wyjazd-za-granice-2/">
	<h5>Wyjazd za granicę<br> a rozliczenie PIT</h5>
	<p><span>Sposób rozliczenia dochodów zagranicznych w Polsce zależy od dwóch czynników; od rezydencji podatkowej oraz od zagranicznego kraju, a ściślej od umowy, jaką ma Polska podpisana z danym krajem w sprawie unikania podwójnego opodatkowania. Rozliczanie dochodów zagranicznych nie należy do łatwych zadań, dlatego polecamy państwu rozliczenie PITu przez internet, a w poniższym artykule przedstawiamy Państwu merytoryczne kwestie rozliczania dochodów zagranicznych.</span></p>
	<br/></a>
</li><li>
	<a href="/wiedza/sytuacje-podatkowe/budowa-lub-remont-domu-zwrot-vat-faktury-po-2014-r./">
	<h5>Budowa lub remont domu (zwrot VAT) - faktury po 2014</h5>
	<p><span>Prawo zwrotu VAT przysługuje osobom, które po 1 styczniu 2014 r. poniosły wydatki (udokumentowane fakturami) na materiały budowlane, na które od 1 maja 2004 r. wzrosła stawka VAT z 7% na 22% lub &nbsp;23%. Jeżeli zakupiliśmy materiały, na które stawka VAT nie wzrosła - nie przysługuje nam odliczenie. Aby odliczyć VAT wszelkie wydatki muszą być udokumentowane fakturami.&nbsp;</span></p>
	<br/></a>
</li><li>
	<a href="/wiedza/poradniki/jak-i-kiedy-otrzymam-zwrot-podatku/">
	<h5>Zwrot podatku</h5>
	<p><span>Na zwrot podatku urzędy skarbowe mają trzy miesiące od dnia otrzymania zeznania. Często zdarza się, że zwrot otrzymujemy już po paru dniach (z reguły urzędy szybciej wysyłają zwrot, gdy podatnicy wysłali rozliczenie PIT online).&nbsp;</span>Gdy dokunujemy korekty PIT - czas na zwrot podatku wydłuża się o kolejne trzy miesiące. Jeśli urząd spóźni się&nbsp;z ustawowym 3-miesięcznym terminem – przysługująca nam kwota wynikająca z zeznania rocznego powinna być powiększona o odsetki.</p>
	<br/></a>
</li></ul><ul id="four"><li>
	<a href="/wiedza/informacje-podatkowe/czy-trzeba-placic-podatek-od-prezentow-pod-choinka/">
	<h5>Upominki spod choinki<br> a podatek</h5>
	<p><span>Prezenty o bardzo dużej wartości podlegają opodatkowaniu podatkiem od spadków i darowizn (wtedy do wykazanie tych dochodów służy specjalny druk PSD, a nie roczne rozliczenie PIT 2016, np. PIT-37, PIT-36). Gdy prezenty są symboliczne - nie płacimy od nich podatków.</span></p>
	<br/></a>
</li><li>
	<a href="/wiedza/sytuacje-podatkowe/zwrot-podatku-z-zagranicy/rozliczenie-zwrotu-podatku-z-zagranicy-w-polskim-pit/">
	<h5>Rozliczenie zwrotu podatku<br>z zagranicy</h5>
	<p><span>Rozliczenie dochodów zagranicznych <br>w polskim PIT zależy od kraju, w którym pracowaliśmy. Podobnie jest z rozliczeniem zwrotu podatku z zagranicy - gdy pracowaliśmy w państwie należącym do jednej grupy, nie musimy wykazywać zwrotu podatku z zagranicy, gdy zwrot podatku otrzymaliśmy od państwa należącego do drugiej grupy - wtedy dochód ten musimy wykazać w polskim PIT. Do wykazania dochodów zagranicznych służy druk PIT-36. Te PIT-y roczne przez internet można wysłać, jak i złożyć osobiście w urzędzie skarbowym.</span></p>
	<br/></a>
</li><li>
	<a href="/wiedza/poradniki/sposoby-opodatkowania/rozliczenie-z-dzieckiem-jako-osoba-samotnie-wychowujaca/">
	<h5>Osoba samotnie wychowująca<br> a rozliczenie PIT</h5>
	<p>Osoby, które wybiorą sposób rozliczenia jako osoba samotnie wychowująca dzieci (<span>zaznaczenie kwadratu nr 4 w PIT-36 lub PIT-37) zapłacą mniejszy podatek, niż gdyby rozliczały się w sposób indywidualny. Aby móc się rozliczyć jako osoba samotnie wychowująca należy spełnić odpowiednie warunki. Jako osoba samotnie wychowująca można rozliczyć się tylko w PIT 36 i w PIT 37. Online można wysłać zeznanie, jak i złożyć osobiście w urzędzie.</span></p>
	<br/></a>
</li></ul>
		
