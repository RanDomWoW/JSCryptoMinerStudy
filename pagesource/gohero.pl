<p>Kamery sportowe oferujemy od 2008 roku, kiedy to importowaliśmy nieznane nikomu w Polsce kamery GoPro. Zasilana paluszkami AAA kamera na kask HERO WIDE zapewniała swoim właścicielom rozdzielczość video na poziomie 512 x 384, co dziś zapewne wywołuje szyderczy uśmiech na Waszych twarzach. Do dzisiaj mamy na pamiątkę jedną sztukę pierwszej GoPro HERO, która przypomina nam o błyskawicznym postępie technologicznym. Choć minęło raptem 9 lat - GoPro ewoluowało z wielkością obrazu od w/w jakości SD, przez HD, FULL HD do niewyobrażalnego standardu 4K (ULTRA HD) w najnowszym modelu GoPro HERO 6 BLACK.</p>
<p>Między innymi dzięki istotnym innowacjom GoPro stało się bardzo szybko najbardziej rozpoznawalną marką, pozostawiając pozostałe kamery do sportów ekstremalnych daleko w tyle. Obecnie używane są w najróżniejszych dyscyplinach na terenie całego świata, w wielu z nich zaś GoPro HERO to oficjalna kamera sportowa. Przyczyną wielkiej popularności są z pewnością najwyższa jakość produktów i wyjątkowa odporność na czynniki zewnętrzne. Ponadto GoPro wytycza standardy w zakresie jakości obrazu i sposobów montażu, tworząc kolejne modele HERO jeszcze lepsze od poprzednich. Nie bez powodu powszechnym stało się określenie "kamery typu GoPro".</p>
<p>Oprócz kamer oferujemy również bardzo popularne drony DJI oraz GoPro wraz z akcesoriami. Na bieżąco uzupełniamy także gamę akcesoriów dodatkowych - wciąż przybywa nowych produktów, które warto są uwagi, jak choćby GoPole, Sandmarc, JOBY, Xsories, REMOVU, Brofish, Lowepro czy SP Gadgets. W ofercie znajdują się również karty pamięć najlepszych producentów - Sandisk czy Lexar.</p>
<p>UWAGA! Wszystkie oferowane przez nas produkty pochodzą z oficjalnej, polskiej dystrybucji. Oznacza to jedno - pełne wsparcie oraz 24 miesięczną gwarancję, realizowaną na terenie naszego kraju.</p>
<p>Najserdeczniej zapraszamy do korzystania z naszych usług, które były, są i będą nastawione przede wszystkim na klienta i jego potrzeby. Bardzo chętnie podzielimy się doświadczeniem i pomożemy w wyborze optymalnej kamery i akcesoriów. Zapraszamy do <a href="/pl/contact">kontaktu z nami</a>.</p>
