 <h2><a href="people.html">Who We Are</a></h2>
    <p> DMLC is a group to collaborate on open-source machine learning projects,
    with a goal of making cutting-edge large-scale machine learning widely available.
    The contributors includes researchers, PhD students and data scientists who are actively working on the field.
    </p>

