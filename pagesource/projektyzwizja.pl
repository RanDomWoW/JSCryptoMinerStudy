    <h2 >Główne kategorie projektów</h2>
    <div class="row projects-grid categories-row">
                    <div class="span-1-4">
                <h3><a href="http://www.projektyzwizja.pl/projekty-domow/nowoczesne-projekty-domow">Nowoczesne projekty domów&nbsp;<i class="ang-rgt"></i></a></h3>
                <div><p>
	To kategoria, kt&oacute;ra najlepiej określa stylistykę naszej oferty. Na rynku projekt&oacute;w gotowych jesteśmy pionierami tego nurtu i najlepiej czujemy się tworząc domy nowoczesne.&nbsp; Wykonujemy projekty dom&oacute;w jednoznacznie nowoczesnych np. parterowe z płaskim dachem, jak r&oacute;wnież projekty łączące tradycyjną formę ze wsp&oacute;łczesnymi akcentami i materiałami. Preferujemy nowoczesne i otwarte wnętrza bez zbędnych podział&oacute;w funkcjonalnych, doświetlone dużymi płaszczyznami przeszkleń.</p>
</div>

            </div>
                    <div class="span-1-4">
                <h3><a href="http://www.projektyzwizja.pl/projekty-domow/projekty-domow-parterowych">Projekty domów parterowych&nbsp;<i class="ang-rgt"></i></a></h3>
                <div><p>
	Projekty dom&oacute;w parterowych charakteryzują się wygodą użytkowania, niską formą doskonale komponującą się z każdym otoczeniem oraz często mniejszymi kosztami budowy. Nie dziwi więc fakt, że ten typ zabudowy zyskuje wielu entuzjast&oacute;w, kt&oacute;rzy świadomi ich zalet coraz częściej decydują się na budowę domu parterowego. Oferujemy projekty o r&oacute;żnej wielkości i odmiennych programach funkcjonalnych. Odwiedź tą popularną kategorię i wybierz jeden z projekt&oacute;w parterowych.</p>
</div>

            </div>
                    <div class="span-1-4">
                <h3><a href="http://www.projektyzwizja.pl/projekty-domow/projekty-domow-z-poddaszem">Projekty domów z poddaszem&nbsp;<i class="ang-rgt"></i></a></h3>
                <div><p>
	To najpopularniejszy typ zabudowy w naszym kraju. R&oacute;wnież w naszej ofercie projekty dom&oacute;w z poddaszem stanowią większość. Sukces dom&oacute;w z dachami spadzistymi jest oczywiście konsekwencją naszego klimatu i wynikającej z niego tradycji budowlanej. Możliwość użytkowego zagospodarowania poddasza to niezaprzeczalna zaleta, kt&oacute;ra redukuje zewnętrzne gabaryty domu ułatwiając jego lokalizację na znacznie mniejszych działkach.</p>
</div>

            </div>
                    <div class="span-1-4">
                <h3><a href="http://www.projektyzwizja.pl/projekty-domow/projekty-domow-pietrowych">Projekty domów piętrowych&nbsp;<i class="ang-rgt"></i></a></h3>
                <div><p>
	Proponujemy tutaj kilka dom&oacute;w piętrowych o zdecydowanie reprezentacyjnym charakterze, przeznaczonych dla najbardziej wymagających klient&oacute;w poszukujących komfortu i elegancji. W ciągu ostatnich kilkunastu lat można zauważyć wzmożony popyt na ten typ zabudowy, kt&oacute;ry rozwija się najczęściej w enklawach dom&oacute;w jednorodzinnych na obrzeżach dużych aglomeracji miejskich. W pełni użytkowa przestrzeń piętra to gł&oacute;wna zaleta popularyzująca tę grupę projekt&oacute;w.</p>
</div>

