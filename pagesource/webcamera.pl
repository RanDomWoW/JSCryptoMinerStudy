$(function () {
    var $listing = $('#inline-camera-listing');
    var no_more_content = false;
    var gathering_in_progress = false;
    var page = 1;
    var used = '';
    var limit;
    var type;
    var id;
    var between_count;
    var delay = 250;

    var countColumns = function () {
        var count = 0;

        count += $listing.find('.inlinecam-fixed, .inline-item:not(.inline-item-wide)').length;
        count += $listing.find('.inline-item-wide').length * 2;

        return count;
    };

    var countCameras = function () {
        var count = 0;

        count += $listing.find('.inlinecam-fixed').length;

        return count;
    };

    var getMore = function () {
        if (gathering_in_progress || no_more_content) {
            return false;
        }
        gathering_in_progress = true;
        page += 1;
        $listing.addClass('show-loader');
        
        $.ajax({
            url: '/ajax/getNextListItems.php',
            method: 'GET',
            data: {
                page: page,
                limit: limit,
                type: type,
                id: id,
                used: used,
                between_count: between_count,
                columns: countColumns(),
                cameras: countCameras()
            },
            success: function (response) {
                if (response.ok && response.last) {
                    $(window).off('.getNextListItems');
                    no_more_content = true;
                    $listing.append(response.html);

                } else if (response.ok && response.html) {
                    $listing.append(response.html);

                } else if (response.ok && !response.html) {
                    $(window).off('.getNextListItems');
                    no_more_content = true;
                }
            },
            complete: function () {
                setTimeout(function () {
                    gathering_in_progress = false;
                    $listing.removeClass('show-loader');
                    scrollCheck();
                }, delay);
            }
        });
    };

    var scrollCheck = function () {
