			<h2>国内首个开源云主机面板</h2>
			AMH 是国内首个开源的主机面板，使用APL开源软件协议。<br />
			这几年来我们一直致力于创新、完善改进产品与响应用户的需求，我们拥有闪亮的用户口碑。<br />
			至今 AMH 系列产品已有十余万次下载安装，相关应用领域也占有国内大部分市场份额。<br />


			<h2>国内首个弹性云主机面板</h2>
			AMH 实现平台化架构设计，高可扩展灵活性，所有功能软件可选择定制下载安装。<br />
			AMH 支持用户自由弹性组建运行环境与切换环境，您可以下载安装不同版本的WEB服务器、数据库、脚本软件自由组合创建您需要的运行环境。<br />
			例如，您可以创建 LNMP、LAMP、LNMH、LNGX、LNAMP等不同WEB应用环境。<br />
			(PHP环境支持所有PHP版本共存，包括PHP5.2、5.3、5.4、5.5、5.6、7.0)<br />

			<h2>国内首个支持组建 SMTP 邮局面板</h2>
			AMH 支持用户组建完全自主的SMTP企业邮局，无限支持域名邮箱绑定，无限邮局账号限制。<br />
			您可自由收发信件，不再需要使用第三方邮件托管服务，保护信件数据隐私，拥有完全灵活自主权。<br />


			<h2>国内首个支持 PHP7 环境面板</h2>
                        目前PHP重大更新版本PHP7已经上线发布。<br />
			PHP7版本运行性能有极大提升，运行速度将是PHP5.6的两倍。<br />
			AMH环境软件已经全面支持PHP7运行，您可以轻松创建多套(lNMP/LAMP)PHP版本的环境同时使用。<br />

			<h2>国内首个支持 HTTP/2 环境面板</h2>
			AMH面板目前Nginx、Apache新版本都已经更进对HTTP/2支持。<br />
                        HTTP/2是HTTP/1.x的最新更新优化版本。<br />
                        HTTP/2加载速度相比旧HTTP/1.x协议有很大的提高（2倍+)。<br />

			<h2>国内首个支持 Apache2.4 环境面板</h2>
			AMH 新版本已推出 Apache2.4 的支持，提供运行最新 Apache2.4 版本。<br />
			AMH Apache2.4 环境使用更好性能的 event 模式与灵活的 php-fpm 进程管理运行。<br />
			AMH 提供所有 PHP 版本支持。 (包括 Apache2.4 不提供支持的 php5.2)<br />


			<h2>国内首个支持 HHVM 环境面板</h2>
			HHVM 是 Facebook 开发的执行 PHP 代码的高性能虚拟机。AMH 面板新版本已推出 HHVM 环境支持。<br />
			HHVM 具有产生快速代码和即时编译的优点，与有极高的运行性能。<br />
