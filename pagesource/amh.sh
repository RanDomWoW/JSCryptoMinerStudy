		<div id="amh_feature_show" class="show_item" >

			<h2>简洁高效</h2>
			AMH 是开源的、轻巧高效的，源码是透明的，AMH 是不依赖任何第三方库独立运行的。<br />
			AMH 已实现平台化设计，我们喜欢简单。默认最小化程序安装。<br />


			<h2>极速安装</h2>
			AMH 提供多镜像支持、自动选择最优速度镜像下载安装，<br />
			AMH 支持 Centos、Debian、Ubuntu 所有发行版本安装，包括最新的 Centos7 与 Debian8 系统。<br />
			此外，AMH 提供极速与编译两种安装方式，极速方式最快可在几十秒内完成安装，您不再需要等待。<br />
			同时对硬件无做限制，例如您可以在 128MB 以下的内存的机器安装。<br />


			<h2>完美用户体验</h2>
			追求完美用户体验一直是我们的目标，AMH 前后端都为自主开发与设计。<br />
			不管是视图的设计或是功能展现的细节，我们在产品上的投入都是全心的。<br />
			AMH 面板是纯净的，在产品上您不会看到有任何广告或无关信息，哪怕是一文字推介链接。<br />


			<h2>安全稳定</h2>
			安全稳定与优秀的用户体验是我们不断的追求，<br />
			AMH 提供HTTPS加密连接使用面板，同时所有下载软件源码都进行MD5效验，我们致力解决任何可能存在的安全风险，<br />
			包括源码篡改、XSS 攻击、CSRF 伪造、SQL 注入，暴力破解、跨站入侵等。<br />


			<h2>最优节能</h2>
			AMH5 版本所有软件都弹性安装，相比以往版本更加节省系统资源，<br />
			AMH 核心程序只有上百KB，系统初始启动只使用 10MB 左右的内存（面板闲时零资源占用），面板毫秒级响应。<br />


			<h2>高扩展性</h2>
			扩展性一直是 AMH 注重的功能之一，<br />
			AMH 提供丰富灵活的模块扩展软件支持，用户按需下载安装软件，扩展面板功能。<br />
			同时 AMH 用户也可以应需求自主开发功能支持。<br />
			后续 AMH 官方会计划推出更多企业级应用扩展程序支持，敬请期待。<br />
