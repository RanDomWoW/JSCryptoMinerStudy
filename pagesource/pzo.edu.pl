jako narzędzie do zarządzania Oświatą przez Organ Prowadzący w Jednostce
Samorządu Terytorialnego (JST), gromadzące w swoim obrębie szereg składowych
modułów. Platforma Zarządzania Oświatą OTAGO wspiera pracę Placówek Oświatowych
na każdym etapie - począwszy od kompleksowej obsługi rekrutacji, poprzez organizację
jednostek oświatowych i inwentaryzację zasobów, zarządzanie pełną informacją
