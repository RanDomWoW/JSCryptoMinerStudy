<p class=MsoNoSpacing><b>Kompleksowy system rekrutacji (KSR)</b></p>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Zapewnia obsługę procesów naboru do przedszkoli i
oddziałów przedszkolnych w szkołach podstawowych, szkół podstawowych, gimnazjów
i szkół ponadgimnazjalnych. Zbudowany jest z uzupełniających się części:
wewnętrznej - dla użytkowników z jednostek oświatowych oraz publicznej -
skierowanej do rodziców oraz kandydatów biorących udział w rekrutacji. System
pozwala na rozległą konfigurację w zakresie kryteriów naboru oraz zakresu gromadzonych
danych osobowych. Wspiera mechanizm obsługi rejonizacji szkół oraz wymiany
danych ze zintegrowaną bazą ewidencji ludności miasta ELUD OTAGO.</span></p>

<p class=MsoNormal style='text-align:justify;line-height:normal'><img
width=330 height=247 id="Obraz 11" src="index_pliki/image006.jpg"
alt="PZO_06.png"></p>

<h4 style='text-align:justify;line-height:normal'><span style='font-size:10.0pt;
font-family:"Calibri","sans-serif";font-style:normal'>Organizacja
pracy jednostki (OPJ)</span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Moduł umożliwia prowadzenie ewidencji placówek
szkolnych i wychowania pozaszkolnego zarządzanych przez JST. Dostarcza
narzędzia do inwentaryzacji zasobów oraz wyposarzenia jednostek, jak również
umożliwia prezentację wybranych danych w zintegrowanym Portalu Oświatowym.
Główną funkcjonalnością modułu jest tworzenie arkusza organizacyjnego jednostki
oświatowej, który stanowi podstawę dla pozostałych modułów.</span></p>

<h4 style='text-align:justify;line-height:normal'><a name="h_f58wa5yplla1"></a><span
style='font-size:10.0pt;font-family:"Calibri","sans-serif";
font-style:normal'>Katalog nauczycieli i pracowników (KNP)</span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Moduł jest rejestrem pracowników i użytkowników
wykorzystywanym we wszystkich częściach systemu PZO OTAGO.<a
name="h_p5e65sc8tf67"></a> Umożliwia w łatwy sposób nadawanie uprawnień do
systemu oraz kontrolowanie dostępu do poszczególnych jego modułów i
funkcjonalności.</span></p>

<h4 style='text-align:justify;line-height:normal'><a name="h_d24nwss9vnxy"></a><span
style='font-size:10.0pt;font-family:"Calibri","sans-serif";
font-style:normal'>System zarządzania uczniem (SZU)</span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Kompleksowe wsparcie w pracy sekretariatów szkolnych gromadzące
informacje o uczniach, ich ścieżce edukacyjnej oraz wynikach kształcenia. Oferuje
dostęp do tworzenia arkuszy ocen, świadectw oraz druku korespondencji seryjnej.
Na podstawie danych z centralnej bazy daje możliwość raportowania kluczowych,
zawsze aktualnych wskaźników dla JST. Dane zgromadzone w SZU są bezpośrednio
wykorzystywane między innymi w podsystemach KSR, DEO, OEZ i AOK.</span></p>

<h4 style='text-align:justify;line-height:normal'><a name="h_iydd9xu8w0yu"></a><span
style='font-size:10.0pt;font-family:"Calibri","sans-serif";
font-style:normal'>Analiza obowiązku kształcenia (AOK) </span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Dzięki bezpośredniej wymianie danych z miejskim
systemem ewidencji ludności miasta, odwzorowaniem obwodów szkolnych i aktualnym
stanem dzieci oraz uczniów z modułu SZU moduł ten pozwala rzetelnie weryfikować
obowiązek kształcenia (obowiązek rocznego przygotowania przedszkolnego, obowiązek
szkolny oraz nauki). Moduł zapewnia prowadzenie oraz drukowanie księgi
ewidencji dzieci, realizuje obsługę procesu egzekwowania spełniania obowiązku
kształcenia, w tym prowadzenie spraw oraz generację pism do opiekunów prawnych.</span></p>

<h4 style='text-align:justify;line-height:normal'><a name="h_uj3middbj53v"></a><span
style='font-size:10.0pt;font-family:"Calibri","sans-serif";
font-style:normal'>Dziennik elektroniczny i organizacja lekcji (DEO) </span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Moduł pozwala na rejestrowanie dziennej frekwencji
ucznia zgodnie z przyporządkowanym planem lekcji, zarządzanie ocenami
cząstkowymi (wg domyślnej skali ocen lub zdefiniowanej przez dyrekcję) oraz
uwagami. Rodzice oraz sami uczniowie otrzymują dostęp do podglądu swoich danych.
Moduł udostępnia także zaawansowany mechanizm automatycznego generowania planu
lekcji dla poszczególnych oddziałów w placówce z uwzględnieniem rozmieszczenia
sal oraz organizacji dyżurów nauczycielskich w oparciu o zdefiniowany rozkład zajęć.</span></p>

<h4 style='text-align:justify;line-height:normal'><a name="h_wpqirhjtnk6r"></a><a
name="h_toi53j6omrtl"></a><span style='font-size:10.0pt;font-family:"Calibri","sans-serif";
font-style:normal'>Organizacja egzaminów zewnętrznych (OEZ)</span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Moduł zapewnia możliwość składania, weryfikacji oraz akceptacji
elektronicznych deklaracji przystąpienia do egzaminu zewnętrznego przez uczniów
ostatnich klas, absolwentów szkół ponadgimnazjalnych oraz przez osoby wskazane
przez OKE. Na podstawie deklaracji system przydziela uczniów do wybranych grup
egzaminacyjnych, sal  wraz z definiowaniem i weryfikacją składu przedmiotowych
zespołów egzaminacyjnych oraz zespołów nadzorujących.</span></p>

<h4 style='text-align:justify;line-height:normal'><a name="h_s07p581nq6fq"></a><span
style='font-size:10.0pt;font-family:"Calibri","sans-serif";
font-style:normal'>Moduł statystyczno-raportowy (MSR)</span></h4>

<p class=MsoNormal style='text-align:justify;line-height:normal'><span
style='font-size:10.0pt'>Moduł odpowiada za przygotowanie wszystkich raportów
statystycznych i imiennych w obrębie całego systemu PZO. Mechanizm ten zapewnia
spójną koncepcję wizualizacji danych, a także możliwości wydruku bądź eksportu
do popularnych formatów. Narzędzie to gromadzi kluczowe dane o uczniach i
jednostce oświatowej na potrzeby procesu przygotowania zestawienia do Systemu
Informacji Oświatowej SIO.</span></p>

